import numpy as np

def calculate_iou(box1, box2):
    # box format: [x1, y1, x2, y2]
    x1_inter = max(box1[0], box2[0])
    y1_inter = max(box1[1], box2[1])
    x2_inter = min(box1[2], box2[2])
    y2_inter = min(box1[3], box2[3])

    inter_width = max(0, x2_inter - x1_inter)
    inter_height = max(0, y2_inter - y1_inter)
    intersection_area = inter_width * inter_height

    box1_area = (box1[2] - box1[0]) * (box1[3] - box1[1])
    box2_area = (box2[2] - box2[0]) * (box2[3] - box2[1])

    union_area = box1_area + box2_area - intersection_area

    if union_area == 0:
        return 0.0
    return intersection_area / union_area

def calculate_metrics(gt_pairs, pred_pairs, iou_threshold=0.5):
    TP = 0
    FP = 0
    FN = 0

    flat_gt_boxes = []
    for label, boxes_list in gt_pairs:
        for box in boxes_list:
            flat_gt_boxes.append(box)

    flat_pred_boxes = []
    for label, boxes_list in pred_pairs:
        for box in boxes_list:
            flat_pred_boxes.append(box)

    if not flat_gt_boxes and not flat_pred_boxes:
        return {'precision': 1.0, 'recall': 1.0, 'f1': 1.0, 'TP': 0, 'FP': 0, 'FN': 0}
    if not flat_gt_boxes and flat_pred_boxes:
        return {'precision': 0.0, 'recall': 0.0, 'f1': 0.0, 'TP': 0, 'FP': len(flat_pred_boxes), 'FN': 0}
    if flat_gt_boxes and not flat_pred_boxes:
        return {'precision': 0.0, 'recall': 0.0, 'f1': 0.0, 'TP': 0, 'FP': 0, 'FN': len(flat_gt_boxes)}

    matched_gt = [False] * len(flat_gt_boxes)

    # Match predictions to ground truth
    for i, p_box in enumerate(flat_pred_boxes):
        best_iou = 0.0
        best_gt_idx = -1
        for j, gt_box in enumerate(flat_gt_boxes):
            if not matched_gt[j]:
                iou = calculate_iou(p_box, gt_box)
                if iou > best_iou:
                    best_iou = iou
                    best_gt_idx = j

        if best_iou >= iou_threshold:
            TP += 1
            matched_gt[best_gt_idx] = True
        else:
            FP += 1

    FN = len(flat_gt_boxes) - sum(matched_gt)

    precision = TP / (TP + FP) if (TP + FP) > 0 else 0.0
    recall = TP / (TP + FN) if (TP + FN) > 0 else 0.0
    f1 = 2 * (precision * recall) / (precision + recall) if (precision + recall) > 0 else 0.0

    return {'precision': precision, 'recall': recall, 'f1': f1, 'TP': TP, 'FP': FP, 'FN': FN}

