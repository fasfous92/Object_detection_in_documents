def parse_pred_boxes(text):
    text = text.strip()

    # Keep only JSON-ish part if extra text is generated
    start = text.find("[")
    end = text.rfind("]")
    if start != -1 and end != -1 and end > start:
        candidate = text[start:end + 1]
    else:
        candidate = text

    out = []

    # Parse list of dicts
    try:
        data = ast.literal_eval(candidate)
        if isinstance(data, list):
            for obj in data:
                if isinstance(obj, dict) and "bbox" in obj and "label" in obj:
                    bbox = obj["bbox"]
                    label = str(obj["label"])
                    if isinstance(bbox, (list, tuple)) and len(bbox) == 4:
                        out.append((label, list(map(float, bbox))))
            return out
    except Exception:
        pass

    # Fallback: detect {"label":"...","bbox":[...]} patterns
    pattern = re.compile(
        r'"label"\s*:\s*"([^"]+)"\s*,\s*"bbox"\s*:\s*\[([^\]]+)\]',
        re.IGNORECASE | re.DOTALL
    )
    for label, bbox_str in pattern.findall(text):
        try:
            bbox = [float(x.strip()) for x in bbox_str.split(",")]
            if len(bbox) == 4:
                out.append((label, bbox))
        except Exception:
            pass

    return out

